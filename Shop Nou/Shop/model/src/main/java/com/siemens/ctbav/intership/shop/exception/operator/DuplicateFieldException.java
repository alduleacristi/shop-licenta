package com.siemens.ctbav.intership.shop.exception.operator;

public class DuplicateFieldException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DuplicateFieldException() {
		super();
		
	}

	public DuplicateFieldException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public DuplicateFieldException(String arg0) {
		super(arg0);
	}

	public DuplicateFieldException(Throwable arg0) {
		super(arg0);
		
	}
	
	

	
}
